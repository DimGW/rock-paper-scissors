# Ввод числа от пользователя
try:
    number = int(input("Введите целое число: "))
    
    # Проверка деления на 5
    if number % 5 == 0:
        print(f"Число {number} делится на 5 без остатка.")
    else:
        print(f"Число {number} не делится на 5 без остатка.")
except ValueError:
    print("Ошибка: Введите корректное целое число!")